package org;

public class Operations {

	public static void main(String[] args) {
		int a=10;
		int b=11;
		int c=a+b;
		long l=122345l;
		System.out.println(c);
		System.out.println(a+b);
		int n=a+(int)l;//typecasting long into int
		System.out.println(n);
		float f=11.2f;
		double d=532.3;
		float f1=f+(float)d;
		System.out.println(f1);
		

	}

}
